    <h1>Sobre o site</h1>
    <br>
    <p>
    Esse site foi criado com o intuito de aplicar o que foi aprendido nas aulas de programação como um PI, tipo um TCC.
    <br><br>
    Origem e Desenvolvimento
    <br> <br>
O site de montagem de computadores surgiu como um projeto final ambicioso das aulas de programação, iniciado por um grupo de estudantes entusiasmados com tecnologia. Eles se inspiraram em uma necessidade real: simplificar o processo de escolha e montagem de computadores personalizados para usuários comuns.
<br> <br>
O projeto começou com uma ideia simples durante as aulas de programação, onde os alunos aprenderam os fundamentos do desenvolvimento web, bancos de dados e design de interfaces. Com o apoio institucional da escola ou universidade, os alunos tiveram acesso a recursos como laboratórios de informática, mentoria de professores e possivelmente financiamento para servidores e domínios.
<br> <br>
<h2>Implementação Técnica</h2>
<br> <br>
O site foi desenvolvido utilizando tecnologias modernas aprendidas nas aulas, como HTML, CSS e JavaScript para a interface do usuário. Para o backend, os alunos aplicaram seus conhecimentos em linguagens como Python, PHP ou Node.js, dependendo da preferência e do currículo educacional. Um banco de dados foi utilizado para armazenar informações sobre peças de hardware disponíveis, preços atualizados e especificações técnicas.
<br> <br>
Além disso, o projeto incorporou APIs de grandes varejistas de tecnologia para verificar disponibilidade de estoque e preços em tempo real. Isso permitiu que o site oferecesse recomendações precisas e atualizadas aos usuários.
<br> <br>
Funcionalidades Principais
<br> <br>
Configuração Personalizada: Os usuários podem selecionar componentes como processador, placa-mãe, memória RAM, placa de vídeo e armazenamento de acordo com suas necessidades e orçamento.
<br> <br>
Comparação de Preços: Integração com APIs de varejistas para mostrar o preço atualizado de cada componente e comparar entre diferentes fornecedores.
<br> <br>
Guias e Tutoriais: Seção educativa com guias passo a passo sobre como montar um computador, dicas de compatibilidade entre componentes e melhores práticas de montagem.
<br> <br>
Feedback da Comunidade: Recursos para os usuários compartilharem suas próprias configurações de computadores e experiências de montagem, promovendo um senso de comunidade e colaboração.
<br>  <br>
Impacto Institucional e Além
<br> <br>
Além de ser um projeto educacional, o site de montagem de computadores serviu como um portfólio impressionante para os alunos envolvidos. Eles puderam demonstrar suas habilidades técnicas em entrevistas de emprego ou ao se candidatar a estágios na indústria de tecnologia.
<br> <br>
Institucionalmente, o projeto destacou a eficácia do currículo de programação da instituição, preparando os alunos não apenas com habilidades teóricas, mas também com experiência prática em desenvolvimento de software em larga escala.
<br> <br>
Conclusão
<br> <br>
Em suma, o site de montagem de computadores não só facilitou a vida dos usuários finais, oferecendo uma maneira simples e educativa de configurar seus próprios sistemas, mas também representou o culminar do aprendizado e aplicação prática dos alunos em um ambiente educacional institucional. Este projeto exemplifica como a educação em programação pode se traduzir em soluções criativas e úteis para problemas do mundo real.

